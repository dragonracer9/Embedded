void tictactoe_write_display(uint32_t red, uint32_t green) {
  // This function uses the SPI peripheral (a serial bus) to
  // write to the LED matrix on the shield.

  // Ensure RCK is low:
  HAL_GPIO_WritePin(MATRIX_RCK_GPIO_Port, MATRIX_RCK_Pin, GPIO_PIN_RESET);

  // Construct 3 byte buffer:
  uint8_t buf[] = {
      red >> 4,
      (red & 0xF) << 4 | (green >> 8),
      green & 0xff,
  };

  // Transmit:
  HAL_SPI_Transmit(&hspi2, buf, 3, 300);

  // Latch:
  HAL_GPIO_WritePin(MATRIX_RCK_GPIO_Port, MATRIX_RCK_Pin, GPIO_PIN_SET);
  HAL_Delay(1);
  HAL_GPIO_WritePin(MATRIX_RCK_GPIO_Port, MATRIX_RCK_Pin, GPIO_PIN_RESET);
}

void tictactoe_write_leds(uint32_t led1, uint32_t led2) {
  // This function sets LED1 and LED2 on the PBL shield.
  HAL_GPIO_WritePin(LED1_GPIO_Port, LED1_Pin, led1);
  HAL_GPIO_WritePin(LED2_GPIO_Port, LED2_Pin, led2);
}

